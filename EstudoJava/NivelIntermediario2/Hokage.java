package NivelIntermediario2;

public class Hokage {

    String nome;
    String vila;
    int idade;
    boolean vivoOuNao;
    int numeroMissoes;
    double saldoBancario;
    double altura;

    //CTRL SHIFT Q para fazer constructor com geral shortcut
    public Hokage(String nome, String vila, int idade, boolean vivoOuNao, int numeroMissoes, double saldoBancario, double altura) {
        this.nome = nome;
        this.vila = vila;
        this.idade = idade;
        this.vivoOuNao = vivoOuNao;
        this.numeroMissoes = numeroMissoes;
        this.saldoBancario = saldoBancario;
        this.altura = altura;
    }
}


